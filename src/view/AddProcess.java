    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        int pId = Integer.parseInt(this.textPId.getText());
        int arrivalTime = Integer.parseInt(this.textArrivalTime.getText());
        int expectedTime = Integer.parseInt(this.textExpectedTime.getText());
        int remainingTime = Integer.parseInt(this.textRemainingTime.getText());
        
        Controller.addProcess(pId, arrivalTime, expectedTime, remainingTime);
        
        String[] columnsPro = {"PID","Arrival Time","Burst Time","Remaining Time"};
        String[][] procDet = Controller.getProcessDetails();
        this.processTable = new JTable(procDet,columnsPro);
        jScrollPane1.setViewportView(processTable);
        
        this.textPId.setText("");
        this.textArrivalTime.setText("");
        this.textExpectedTime.setText("");
        this.textRemainingTime.setText("");
    }//GEN-LAST:event_buttonAddActionPerformed

